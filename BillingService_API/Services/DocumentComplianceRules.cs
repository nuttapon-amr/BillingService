    public static string ResolveThaiDocumentTitle(string documentType)
    {
        return documentType switch
        {
            "RC" => "ใบเสร็จรับเงิน",
            "TI" => "ใบกำกับภาษี",
            "CN" => "ใบลดหนี้",
            _ => documentType
        };
    }
